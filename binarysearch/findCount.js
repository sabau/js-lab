const findCount = function(A, B){
  var l = A.length;
  var idx = Math.round(l/2);
  if (l === 0) return 0;
  if (l === 1) return A[0] === B ? 1 : 0;
  if (A[idx] < B) {
    return findCount(A.splice(idx, l), B);
  } else if (A[idx] > B) {
    return findCount(A.splice(0, idx), B);
  }
  var count = 1;

  if (idx >= 1) {
    for (var i = idx-1; A[i] === B && i >= 0; i--) {
      count += 1;
    }
  }
  if (idx < l) {
    for (var i = idx+1; A[i] === B && i < l; i++) {
      count += 1;
    }
  }
  return count;
}
